public enum KeySymbol: Int {
  case Backspace = 0xff08 //
  case Return = 0xff0d
  case QuoteLeft = 0x0060 // 反引号(`), 标准键盘数字1左边的键
  case Plus = 0x002b // +
  case Minus = 0x002d // -
  case Comma = 0x002c // ,
  case Period = 0x002e // .
  case BracketLeft = 0x005b // [
  case BracketRight = 0x005d // ]
}
